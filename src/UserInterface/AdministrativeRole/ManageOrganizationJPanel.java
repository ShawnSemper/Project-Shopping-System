    private void populateCombo(){
        organizationJComboBox.removeAllItems();
        if(enterprise.getEnterpriseType().getValue().equals(Enterprise.EnterpriseType.Bank.getValue())){
            organizationJComboBox.addItem(Organization.Type.AccountOwner);
        }else if(enterprise.getEnterpriseType().getValue().equals(Enterprise.EnterpriseType.ShoppingPlatform.getValue())){
            organizationJComboBox.addItem(Organization.Type.Customer);
            organizationJComboBox.addItem(Organization.Type.Seller);
        }else if(enterprise.getEnterpriseType().getValue().equals(Enterprise.EnterpriseType.DeliveryCo.getValue())){
            organizationJComboBox.addItem(Organization.Type.Deliveryman);
        }
    }

    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            Object[] row = new Object[2];
            row[0] = organization;
            row[1] = organization.getOrganizationType();
            
            model.addRow(row);
        }
    }
