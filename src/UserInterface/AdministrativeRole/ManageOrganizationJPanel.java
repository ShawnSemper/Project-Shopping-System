    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = organizationJTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select an organization first", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            Organization target = (Organization) organizationJTable.getValueAt(selectedRow, 0);
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete the organization?", "Warning", dialogButton);
            if (dialogResult == JOptionPane.YES_OPTION) {
                enterprise.getOrganizationDirectory().getOrganizationList().remove(target);
                populateTable();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

