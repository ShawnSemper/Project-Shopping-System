    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        Type type = (Type) organizationJComboBox.getSelectedItem();
        Organization newOrganization = enterprise.getOrganizationDirectory().createOrganization(type);
        
        String name = nameJTextField.getText();
        if (name.equals("")) {
           JOptionPane.showMessageDialog(null, "please set name!");
           return;
            
        }
        newOrganization.setName(name);
        JOptionPane.showMessageDialog(null, "Create organization success!");
        nameJTextField.setText("");
        populateTable();
    }//GEN-LAST:event_addJButtonActionPerformed

