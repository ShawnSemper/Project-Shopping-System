    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblPayments.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select an payment to process", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            Order order = (Order)tblPayments.getValueAt(selectedRow, 0);
            Order.PaymentStatus status = (Order.PaymentStatus)tblPayments.getValueAt(selectedRow, 4);
            if(!status.equals(Order.PaymentStatus.Pending)){
                JOptionPane.showMessageDialog(null, "This payment has already been processed", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
            ProcessPaymentJPanel panel = new ProcessPaymentJPanel(this.userProcessContainer, order, userAccount, business);
            userProcessContainer.add("ProcessOrderJPanel", panel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnProcessActionPerformed


