    public void populateOrders(){
        DefaultTableModel dtm = (DefaultTableModel) tblPayments.getModel();
        dtm.setRowCount(0);
        for(Order a : userAccount.getOrders()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = a;
            row[1] = a.getCustomer();
            row[2] = a.getSeller();
            row[3] = a.getTotalPrice();
            row[4] = a.getPaymentStatus();
            dtm.addRow(row);
        }
    } 
