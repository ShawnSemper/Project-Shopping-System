    public void populateOrder(){
        txtContact.setText(order.getContactInfo());
        txtCustomer.setText(order.getCustomer().toString());
        txtDate.setText(order.getCreateDate().toString());
        txtID.setText(String.valueOf(order.getId()));
        txtName.setText(order.getProduct());
        txtPrice.setText(String.valueOf(order.getPrice()));
        txtQuantity.setText(String.valueOf(order.getQuantity()));
        txtTotal.setText(String.valueOf(order.getTotalPrice()));
        txtTo.setText(order.getShippingTo());
        
        DeliveryCombo.removeAllItems();
        for(Network network:business.getNetworkList()){
            for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList()){
                if(enterprise instanceof DeliveryCoEnterprise){
                    DeliveryCombo.addItem(enterprise.toString());
                }
            }
        }    
    }
