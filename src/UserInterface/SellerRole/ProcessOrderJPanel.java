    private void btnDeclineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeclineActionPerformed
        // TODO add your handling code here:
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure to decline the order?", "Warning", dialogButton);
        if(dialogResult == JOptionPane.YES_OPTION){
            // change the paymentStatus
            order.setSellerStatus(Order.SellerStatus.Fail);
            // change the Order Status
            order.setStatus(Order.Status.Fail);
            
            loadPreviousPage();
        }
    }//GEN-LAST:event_btnDeclineActionPerformed

