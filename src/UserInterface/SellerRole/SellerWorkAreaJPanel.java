    public void populateProducts(){
        DefaultTableModel dtm = (DefaultTableModel) tblProducts.getModel();
        dtm.setRowCount(0);
        for(Product a : userAccount.getProducts()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = a;
            row[1] = a.getStock();
            dtm.addRow(row);
        }
    }
     
    public void populateOrders(){
        DefaultTableModel dtm = (DefaultTableModel) tblOrder.getModel();
        dtm.setRowCount(0);
        for(Order a : userAccount.getOrders()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = a;
            row[1] = a.getCustomer();
            row[2] = a.getProduct();
            row[3] = a.getQuantity();
            row[4] = a.getTotalPrice();
            row[5] = a.getSellerStatus();
            dtm.addRow(row);
        }
    } 
    
