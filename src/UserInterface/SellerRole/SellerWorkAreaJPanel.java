    private void btnDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProducts.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a product to view", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            Product product = (Product)tblProducts.getValueAt(selectedRow, 0);
            ProductDetailJPanel panel = new ProductDetailJPanel(this.userProcessContainer, userAccount, enterprise, product);
            userProcessContainer.add("ProductDetailJPanel", panel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnDetailActionPerformed

    private void btnProcessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblOrder.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select an order to process", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            Order order = (Order)tblOrder.getValueAt(selectedRow, 0);
            Order.SellerStatus status = (Order.SellerStatus)tblOrder.getValueAt(selectedRow, 5);
            if(!status.equals(Order.SellerStatus.Pending)){
                JOptionPane.showMessageDialog(null, "This order has already been processed", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }
            ProcessOrderJPanel panel = new ProcessOrderJPanel(this.userProcessContainer, order, userAccount, business);
            userProcessContainer.add("ProcessOrderJPanel", panel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnProcessActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProducts.getSelectedRow();
        if(selectedRow >= 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete the Product?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                Product product = (Product) tblProducts.getValueAt(selectedRow, 0);
                userAccount.getProducts().remove(product);
                platform.getProducts().remove(product);
                populateProducts();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Please select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


