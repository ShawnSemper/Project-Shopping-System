    public void populateProducts(){
        DefaultTableModel dtm = (DefaultTableModel) tblProducts.getModel();
        dtm.setRowCount(0);
        for(Product a : platform.getProducts()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = a.getSeller();
            row[1] = a;
            row[2] = a.getPrice();
            row[3] = a.getStock();
            dtm.addRow(row);
        }
    }
    
