    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProducts.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a product to view", "Warning", JOptionPane.WARNING_MESSAGE);
        }else{
            Product product = (Product)tblProducts.getValueAt(selectedRow, 1);
            ProductDetail2JPanel panel = new ProductDetail2JPanel(this.userProcessContainer, product, userAccount, enterprise, business);
            userProcessContainer.add("ProductDetail2JPanel", panel);
            CardLayout layout = (CardLayout)userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void loadPreviousPage() {
        userProcessContainer.remove(this);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }

